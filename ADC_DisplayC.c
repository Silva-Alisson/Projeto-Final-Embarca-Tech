#define I2C_PORT i2c1
#define I2C_SDA 14
#define I2C_SCL 15
#define OLED_ADDRESS 0x3C
#define LED_GREEN 11
#define SW_PIN 22
#define BOTAO_A 5
#define BOTAO_B 6

// Função para inicializar o display OLED
void init_oled(ssd1306_t *ssd) {
    i2c_init(I2C_PORT, 400 * 1000);
    gpio_set_function(I2C_SDA, GPIO_FUNC_I2C);
    gpio_set_function(I2C_SCL, GPIO_FUNC_I2C);
    gpio_pull_up(I2C_SDA);
    gpio_pull_up(I2C_SCL);

    ssd1306_init(ssd, WIDTH, HEIGHT, false, OLED_ADDRESS, I2C_PORT);
    ssd1306_config(ssd);
    ssd1306_fill(ssd, false); // Limpa o display
    ssd1306_send_data(ssd);
}

// Função para inicializar os botões
void init_botoes() {
    gpio_init(SW_PIN);
    gpio_set_dir(SW_PIN, GPIO_IN);
    gpio_pull_up(SW_PIN);

    gpio_init(BOTAO_A);
    gpio_set_dir(BOTAO_A, GPIO_IN);
    gpio_pull_up(BOTAO_A);

    gpio_init(BOTAO_B);
    gpio_set_dir(BOTAO_B, GPIO_IN);
    gpio_pull_up(BOTAO_B);

    gpio_init(LED_GREEN);
    gpio_set_dir(LED_GREEN, GPIO_OUT);
    gpio_put(LED_GREEN, false);
}

void ssd1306_pixel_4x(ssd1306_t *ssd, uint8_t x, uint8_t y, bool value) {
  for (uint8_t i = 0; i < 2; i++) {
      for (uint8_t j = 0; j < 2; j++) {
          ssd1306_pixel(ssd, x + i, y + j, value);
      }
  }
}

void ssd1306_draw_matrix(ssd1306_t *ssd, uint8_t matrix[][40], uint8_t rows, uint8_t cols) {
  for (uint8_t i = 0; i < rows; i++) {
      for (uint8_t j = 0; j < cols; j++) {
          if (matrix[i][j] == 1) {
              ssd1306_pixel(ssd, j, i, true);
          }
      }
  }
}

